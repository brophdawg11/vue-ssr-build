/* eslint-disable no-console */

const fs = require('fs');
const path = require('path');

/* eslint-disable import/no-extraneous-dependencies */
const MFS = require('memory-fs');
const webpack = require('webpack');
const chokidar = require('chokidar');
const webpackHotMiddleware = require('webpack-hot-middleware');
const webpackDevMiddleware = require('webpack-dev-middleware');
/* eslint-enable import/no-extraneous-dependencies */

const clientConfig = require('../build/webpack.client.config');
const serverConfig = require('../build/webpack.server.config');

const readFile = (_fs, file) => {
    try {
        return _fs.readFileSync(path.join(clientConfig.output.path, file), 'utf-8');
    } catch (e) {
        return null;
    }
};

module.exports = function setupDevServer(app, config, cb) {
    let bundle;
    let template;
    let clientManifest;

    let ready;

    const readyPromise = new Promise(r => { ready = r; });
    const update = () => {
        if (bundle && clientManifest) {
            ready();
            cb(bundle, {
                template,
                clientManifest,
            });
        }
    };

    // read template from disk and watch
    template = fs.readFileSync(config.templatePath, 'utf-8');
    chokidar.watch(config.templatePath).on('change', () => {
        template = fs.readFileSync(config.templatePath, 'utf-8');
        update();
    });

    // modify client config to work with hot middleware
    clientConfig.entry.app = [
        'webpack-hot-middleware/client',
        clientConfig.entry.app,
    ];
    clientConfig.output.filename = '[name].js';
    clientConfig.plugins.push(
        new webpack.HotModuleReplacementPlugin(),
        new webpack.NoEmitOnErrorsPlugin(),
    );

    // dev middleware
    const clientCompiler = webpack(clientConfig);
    const devMiddleware = webpackDevMiddleware(clientCompiler, {
        publicPath: clientConfig.output.publicPath,
        noInfo: true,
    });
    app.use(devMiddleware);
    clientCompiler.plugin('done', stats => {
        const json = stats.toJson();
        json.errors.forEach(err => console.error(err));
        json.warnings.forEach(err => console.warn(err));
        if (json.errors.length) {
            return;
        }
        clientManifest = JSON.parse(readFile(
            devMiddleware.fileSystem,
            path.basename(config.clientManifest),
        ));
        update();
    });

    // hot middleware
    app.use(webpackHotMiddleware(clientCompiler, { heartbeat: 5000 }));

    // watch and update server renderer
    const serverCompiler = webpack(serverConfig);
    const mfs = new MFS();
    serverCompiler.outputFileSystem = mfs;
    serverCompiler.watch({}, (err, stats) => {
        if (err) {
            throw err;
        }
        const json = stats.toJson();
        if (json.errors.length) {
            return;
        }

        // read bundle generated by vue-ssr-webpack-plugin
        bundle = JSON.parse(readFile(mfs, path.basename(config.serverBundle)));
        update();
    });

    return readyPromise;
};
